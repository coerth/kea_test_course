def sum(number_1: int, number_2: int) -> int:
    return number_1 + number_2

def subtract(number_1: int, number_2: int) -> int:
    return number_1 - number_2

def multiply(number_1: int, number_2: int) -> int:
    return number_1 * number_2

def divide(number_1: int, number_2: int) -> float:
    return number_1 / number_2